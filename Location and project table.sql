SELECT p.project_id, p.project_name, l.city, l.country
FROM projects p
INNER JOIN locations l ON p.project_id = l.location_id
WHERE l.country = 'USA';

SELECT l.location_id, l.city, p.project_name, p.start_date
FROM locations l
LEFT JOIN projects p ON l.location_id = p.project_id
WHERE l.country IN ('USA', 'Canada')
AND (p.start_date > '2023-06-01' OR p.start_date IS NULL);

SELECT p.project_id, p.project_name, l.city, l.state
FROM locations l
RIGHT JOIN projects p ON l.location_id = p.project_id
WHERE p.department_id IN (1, 2, 3)
AND (l.state IS NOT NULL OR p.lead_employee_id > 200);

SELECT l.location_id, l.city, p.project_id, p.project_name
FROM locations l
FULL JOIN projects p ON l.location_id = p.project_id
WHERE (l.country = 'France' OR p.project_name LIKE '%Marketing%')
AND (l.location_id BETWEEN 70 AND 80 OR p.project_id BETWEEN 70 AND 80);

